package com.z21.be.models;

public class SchoolInfo {
	
	private Long schoolId;
	
	private String schoolCode;
	
	private String schoolName;
	
	private String schoolDescription;
	
	private String contactInfo;
	
	private String contactNumber;
	
	private String emailAddress;
	
	private String headerBgColor;
	
	private String headerTextColor;
	
	private String footerBgColor;
	
	private String footerTextColor;
	
	private String address;
	
	private String logo;

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public Long getSchoolId() {
		return schoolId;
	}

	public void setSchoolId(Long schoolId) {
		this.schoolId = schoolId;
	}

	public String getSchoolCode() {
		return schoolCode;
	}

	public void setSchoolCode(String schoolCode) {
		this.schoolCode = schoolCode;
	}

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}


	public String getSchoolDescription() {
		return schoolDescription;
	}

	public void setSchoolDescription(String schoolDescription) {
		this.schoolDescription = schoolDescription;
	}

	public String getContactInfo() {
		return contactInfo;
	}

	public void setContactInfo(String contactInfo) {
		this.contactInfo = contactInfo;
	}

	public String getContactNumber() {
		return contactNumber;
	}

	public void setContactNumber(String contactNumber) {
		this.contactNumber = contactNumber;
	}

	public String getEmailAddress() {
		return emailAddress;
	}

	public void setEmailAddress(String emailAddress) {
		this.emailAddress = emailAddress;
	}

	public String getHeaderBgColor() {
		return headerBgColor;
	}

	public void setHeaderBgColor(String headerBgColor) {
		this.headerBgColor = headerBgColor;
	}

	public String getHeaderTextColor() {
		return headerTextColor;
	}

	public void setHeaderTextColor(String headerTextColor) {
		this.headerTextColor = headerTextColor;
	}

	public String getFooterBgColor() {
		return footerBgColor;
	}

	public void setFooterBgColor(String footerBgColor) {
		this.footerBgColor = footerBgColor;
	}

	public String getFooterTextColor() {
		return footerTextColor;
	}

	public void setFooterTextColor(String footerTextColor) {
		this.footerTextColor = footerTextColor;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}
	
	
	
}
